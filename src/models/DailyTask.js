import mongoose from 'mongoose';

const DailyTaskSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  isCompleted: {
    type: Boolean,
    default: false,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.models.DailyTask || mongoose.model('DailyTask', DailyTaskSchema);
